package Encapsulation;

public class Encapsulate {
	
private String name;
private int roll;
private int age;
	
	public int getAge(){
		return age;
	}
	public String getName(){
		return name;
	}	
	public int getRoll(){
		return roll;
	}
	
	public void setName(String name){
		this.name=name;
	}
	
	public void setRoll(int roll){
		this.roll=roll;
	}
	
	public void setAge(int age){
		this.age=age;
	}

	}
	

