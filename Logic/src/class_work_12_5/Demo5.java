public class Demo5 {
	public static void main(String[] args) { 
		for (int i=8;i>=2;i=i-2){
		for(int j=1; j<=i;j=j+1){
			System.out.print("$");
		}
	System.out.println();	
	}

		System.out.println("************");
		for(int k=8;k>=2;k=k-2) {
			for(int f=1;f<=k;f++) {
				System.out.print("&");
			}
		System.out.println();
		}
	}
	
}
