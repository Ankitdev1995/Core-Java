package com.ankit.parallelStream;

public class Student {
	String name;
	int score;

	public Student() {

	}

	public Student(String name, int score) {
		super();
		this.name = name;
		this.score = score;
	}

	public String getName() {
		return this.name;
	}

	public int getScore() {
		return this.score;
	}
}
